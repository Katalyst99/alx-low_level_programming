#include "3-calc.h"

/**
 * op_add - The addition of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - The subtraction of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - The multipication of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - The division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The result of the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  The remainder of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
